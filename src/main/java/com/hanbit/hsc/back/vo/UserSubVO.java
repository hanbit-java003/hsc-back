package com.hanbit.hsc.back.vo;

public class UserSubVO {

	private String visited;
	private String visitedImg;
	private int userNo;
	private String userId;
	private String usersImg;

	public String getVisited() {
		return visited;
	}

	public void setVisited(String visited) {
		this.visited = visited;
	}

	public String getVisitedImg() {
		return visitedImg;
	}

	public void setVisitedImg(String visitedImg) {
		this.visitedImg = visitedImg;
	}

	public int getUserNo() {
		return userNo;
	}

	public void setUserNo(int userNo) {
		this.userNo = userNo;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUsersImg() {
		return usersImg;
	}

	public void setUsersImg(String usersImg) {
		this.usersImg = usersImg;
	}

}
